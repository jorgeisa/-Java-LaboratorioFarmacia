    private void btnRegistrarMedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarMedActionPerformed
        String nombre = txtNombre.getText();
        String sustancia = txtSustancia.getText();
        String forma = txtForma.getText();
        String presentacion = txtPresentacion.getText();
        String precio = txtPrecio.getText();
        
        Medicamento med = new Medicamento();
        med.registrarMedicamento(nombre, sustancia, forma, presentacion, precio);
        med.mostrarMedicamentos(tableMedicamentos);
        
        txtNombre.setText("");
        txtSustancia.setText("");
        txtForma.setText("");
        txtPresentacion.setText("");
        txtPrecio.setText("");
        
    }//GEN-LAST:event_btnRegistrarMedActionPerformed

